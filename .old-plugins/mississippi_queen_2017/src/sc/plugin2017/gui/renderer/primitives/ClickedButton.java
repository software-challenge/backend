package sc.plugin2017.gui.renderer.primitives;

/**
 * Represents which button is clicked (see
 * {@link sc.plugin2017.gui.renderer.FrameRenderer#mouseClicked()}
 * ). Value NONE indicates that no button was clicked.
 */
public enum ClickedButton {
  NONE, LEFT, RIGHT, SPEED_UP, SPEED_DOWN, SEND, CANCEL
}
